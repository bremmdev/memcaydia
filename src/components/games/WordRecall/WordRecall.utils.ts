//get 10 random words from the list of words
//get up to 5 random words from the list of words that have been seen
//combine the two lists and get a random word from the combined list
export function getRandomWord(word_list: Array<string>, wordsSeen: Array<string>) {
  const randomWords = word_list.sort(() => Math.random() - 0.5).slice(0, 10);

  const randomSeenWords = wordsSeen
    .sort(() => Math.random() - 0.5)
    .slice(0, 5);

  const randomWordsMix = randomWords
    .concat(randomSeenWords)
    .sort(() => Math.random() - 0.5)
    .slice(0, 10);

  const randomWord =
    randomWordsMix[Math.floor(Math.random() * randomWordsMix.length)];

  return randomWord;
}

